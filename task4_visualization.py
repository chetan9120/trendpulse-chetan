import pandas as pd
import numpy as np

def analyze_trend_data():
    # ==========================================
    # TASK 1: Load and Explore
    # ==========================================

    # 1. Load the clean CSV into a Pandas DataFrame
    try:
        df = pd.read_csv("data/trends_clean.csv")
    except FileNotFoundError:
        print("Error: data/trends_clean.csv not found. Please run Task 2 first.")
        return

    # 2. Print the shape (rows, columns)
    print(f"Loaded data: {df.shape}")

    # 3. Print the first 5 rows
    print("First 5 rows:")
    print(df.head())

    # 4. Calculate and print average score and comments using Pandas
    avg_score = df['score'].mean()
    avg_comments = df['num_comments'].mean()

    # The :,.0f formats the numbers with commas and 0 decimal places
    print(f"\nAverage score    : {avg_score:,.0f}")
    print(f"Average comments : {avg_comments:,.0f}")

    # ==========================================
    # TASK 2: Basic Analysis with NumPy
    # ==========================================
    print("\n--- NumPy Stats ---")

    # Extract the 'score' column as a pure NumPy array for calculations
    scores_array = df['score'].to_numpy()

    # Use NumPy methods to find the required statistics
    mean_score = np.mean(scores_array)
    median_score = np.median(scores_array)
    std_score = np.std(scores_array)
    max_score = np.max(scores_array)
    min_score = np.min(scores_array)

    print(f"Mean score    : {mean_score:,.0f}")
    print(f"Median score  : {median_score:,.0f}")
    print(f"Std deviation : {std_score:,.0f}")
    print(f"Max score     : {max_score:,.0f}")
    print(f"Min score     : {min_score:,.0f}")

    # Find which category has the most stories
    # value_counts() sorts from highest to lowest automatically.
    # idxmax() grabs the name of the top one, max() grabs the number.
    top_category = df['category'].value_counts().idxmax()
    top_cat_count = df['category'].value_counts().max()
    print(f"Most stories in: {top_category} ({top_cat_count} stories)")

    # Find the story with the most comments
    # idxmax() on a column gives us the row index number of the highest value
    most_comments_index = df['num_comments'].idxmax()
    top_story_title = df.loc[most_comments_index, 'title']
    top_story_comments = df.loc[most_comments_index, 'num_comments']
    print(f'Most commented story: "{top_story_title}" — {top_story_comments:,.0f} comments')

    # ==========================================
    # TASK 3: Add New Columns
    # ==========================================

    # 1. Calculate engagement: num_comments / (score + 1)
    # Pandas applies this formula to every row simultaneously (vectorization)
    df['engagement'] = df['num_comments'] / (df['score'] + 1)

    # 2. Boolean column: True if score is higher than the average, else False
    df['is_popular'] = df['score'] > avg_score

    # ==========================================
    # TASK 4: Save the Result
    # ==========================================

    output_csv = "data/trends_analysed.csv"
    print(f"\nSaved to {output_csv}")

    # Save to a new CSV file, omitting the index column
    df.to_csv(output_csv, index=False)

# Run the script
if __name__ == "__main__":
    analyze_trend_data()



