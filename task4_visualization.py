import pandas as pd
import glob
import os

def clean_trend_data():
    # ==========================================
    # TASK 1: Load the JSON File
    # ==========================================

    # Use glob to find all json files in the data folder
    json_files = glob.glob('data/trends_*.json')

    # Safety check: Make sure Task 1 actually created a file
    if not json_files:
        print("Error: No JSON files found in data/. Please run Task 1 first.")
        return

    # Find the most recently modified file (in case you ran Task 1 multiple times)
    latest_file = max(json_files, key=os.path.getctime)

    # Load the JSON into a Pandas DataFrame
    df = pd.read_json(latest_file)
    print(f"Loaded {len(df)} stories from {latest_file}")
    # ==========================================
    # TASK 2: Clean the Data
    # ==========================================

    # 1. Duplicates - keep the first instance, remove any other rows with the same post_id
    df = df.drop_duplicates(subset=['post_id'])
    print(f"After removing duplicates: {len(df)}")

    # 2. Missing values - drop the entire row if post_id, title, or score are missing (NaN)
    df = df.dropna(subset=['post_id', 'title', 'score'])
    print(f"After removing nulls: {len(df)}")

    # 3. Low quality - keep only the rows where the score is 5 or higher
    df = df[df['score'] >= 5]
    print(f"After removing low scores: {len(df)}")

    # 4. Data types - Ensure score and num_comments are whole integers
    # Note: If a story had 0 comments, it might be NaN, so we fill it with 0 first
    df['score'] = df['score'].astype(int)
    df['num_comments'] = df['num_comments'].fillna(0).astype(int)

    # 5. Whitespace - strip accidental spaces from the very beginning or end of the titles
    df['title'] = df['title'].str.strip()

    # ==========================================
    # TASK 3: Save as CSV
    # ==========================================

    # Define the output path
    output_csv = 'data/trends_clean.csv'

    # Save to CSV. index=False prevents Pandas from writing the row numbers into the file
    df.to_csv(output_csv, index=False)
    print(f"Saved {len(df)} rows to {output_csv}")

    # Print the final summary of stories per category
    print("\nStories per category:")

    # value_counts() automatically counts occurrences, to_string() makes it print cleanly
    category_counts = df['category'].value_counts()
    print(category_counts.to_string())

# Run the function
if __name__ == "__main__":
    clean_trend_data()

