import pandas as pd
import matplotlib.pyplot as plt
import os

def visualize_trends():
    # ==========================================
    # TASK 1: Setup
    # ==========================================

    # 1. Create the outputs folder if it doesn't exist
    os.makedirs("outputs", exist_ok=True)

    # 2. Load the analyzed data from Task 3
    try:
        df = pd.read_csv("data/trends_analysed.csv")
    except FileNotFoundError:
        print("Error: data/trends_analysed.csv not found. Please run Task 3 first.")
        return

    print("Loaded data successfully. Generating charts...")

    # ==========================================
    # TASK 2: Chart 1 - Top 10 Stories by Score
    # ==========================================

    # Sort the dataframe by score in descending order and grab the top 10
    top_10 = df.sort_values(by='score', ascending=False).head(10).copy()

    # Truncate titles to 50 characters so they fit on the y-axis
    # We use a lambda function to slice the string and add "..." if it's too long
    top_10['short_title'] = top_10['title'].apply(lambda x: x[:47] + '...' if len(x) > 50 else x)

    # Create the figure
    plt.figure(figsize=(10, 6))

    # Create horizontal bar chart
    plt.barh(top_10['short_title'], top_10['score'], color='skyblue')

    # Invert y-axis so the highest score is at the top of the chart, not the bottom
    plt.gca().invert_yaxis()

    # Add labels and title
    plt.title("Top 10 Stories by Score")
    plt.xlabel("Score (Upvotes)")
    plt.ylabel("Story Title")

    # Save BEFORE plt.show(). bbox_inches='tight' prevents labels from being cut off.
    plt.savefig('outputs/chart1_top_stories.png', bbox_inches='tight')
    plt.close() # Close the figure to free up memory
    print("Saved outputs/chart1_top_stories.png")

    # ==========================================
    # TASK 3: Chart 2 - Stories per Category
    # ==========================================

    # Count how many stories belong to each category
    category_counts = df['category'].value_counts()

    plt.figure(figsize=(8, 6))

    # Create the bar chart, assigning a list of colors for variety
    colors = ['#ff9999', '#66b3ff', '#99ff99', '#ffcc99', '#c2c2f0']
    plt.bar(category_counts.index, category_counts.values, color=colors[:len(category_counts)])

    plt.title("Number of Stories per Category")
    plt.xlabel("Category")
    plt.ylabel("Number of Stories")

    # Save
    plt.savefig('outputs/chart2_categories.png', bbox_inches='tight')
    plt.close()
    print("Saved outputs/chart2_categories.png")

    # ==========================================
    # TASK 4: Chart 3 - Score vs Comments
    # ==========================================

    plt.figure(figsize=(10, 6))

    # To colour dots differently based on a condition, the easiest way is to plot them in two separate batches
    popular_df = df[df['is_popular'] == True]
    normal_df = df[df['is_popular'] == False]

    # Plot the normal stories (Blue)
    plt.scatter(normal_df['score'], normal_df['num_comments'], alpha=0.6, label="Normal", color='blue')

    # Plot the popular stories (Orange)
    plt.scatter(popular_df['score'], popular_df['num_comments'], alpha=0.8, label="Popular", color='darkorange')

    plt.title("Score vs. Number of Comments")
    plt.xlabel("Score")
    plt.ylabel("Number of Comments")

    # Add the legend to explain the colours
    plt.legend()

    # Save
    plt.savefig('outputs/chart3_scatter.png', bbox_inches='tight')
    plt.close()
    print("Saved outputs/chart3_scatter.png")

    # ==========================================
    # BONUS: Dashboard (Combining all 3)
    # ==========================================

    # Create a 2x2 grid of subplots. (figsize dictates the total width/height in inches)
    fig, axes = plt.subplots(nrows=2, ncols=2, figsize=(16, 12))
    fig.suptitle("TrendPulse Dashboard", fontsize=20, fontweight='bold')

    # --- Top Left (axes[0, 0]): Top 10 Stories ---
    axes[0, 0].barh(top_10['short_title'], top_10['score'], color='skyblue')
    axes[0, 0].invert_yaxis()
    axes[0, 0].set_title("Top 10 Stories by Score")
    axes[0, 0].set_xlabel("Score")

    # --- Top Right (axes[0, 1]): Categories ---
    axes[0, 1].bar(category_counts.index, category_counts.values, color=colors[:len(category_counts)])
    axes[0, 1].set_title("Stories per Category")
    axes[0, 1].set_ylabel("Count")

    # --- Bottom Left (axes[1, 0]): Scatter Plot ---
    axes[1, 0].scatter(normal_df['score'], normal_df['num_comments'], alpha=0.6, label="Normal", color='blue')
    axes[1, 0].scatter(popular_df['score'], popular_df['num_comments'], alpha=0.8, label="Popular", color='darkorange')
    axes[1, 0].set_title("Score vs Comments")
    axes[1, 0].set_xlabel("Score")
    axes[1, 0].set_ylabel("Comments")
    axes[1, 0].legend()

    # --- Bottom Right (axes[1, 1]): Empty / Cleanup ---
    # We only have 3 charts, so we hide the 4th quadrant to make it look clean
    axes[1, 1].axis('off')

    # Adjust spacing so titles and labels don't overlap
    plt.tight_layout(rect=[0, 0.03, 1, 0.95])

    # Save the master dashboard
    plt.savefig('outputs/dashboard.png', bbox_inches='tight')
    plt.close()
    print("Saved outputs/dashboard.png")

    print("\nVisualization Pipeline Complete! All 4 files saved in the outputs/ directory.")

# Run the script
if __name__ == "__main__":
    visualize_trends()